//! VSOP87B → DE406 轨道改正（Chapront 2000 混合项形式）：在 VSOP→FK5→ICRS 之后施用。
//! 模型：Δσ(T) = (a0+a1*T+a2*T²+a3*T³) + Σ [(C0_i+C1_i*T)cos(f_i*T) + (S0_i+S1_i*T)sin(f_i*T)]。
//! T = (JD − J2000)/365250 儒略千年。DE406 ICRS 修正项。

